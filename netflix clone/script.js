function toggleHide() {
    let btn = document.getElementById('btn1');
    let description = document.getElementById('description1')
    if (description.style.display != 'none') {
        description.style.display = 'none';
        btn1.innerHTML = '+';
    }
    else{
        description.style.display = 'block';
        btn1.innerHTML = 'x';
    }
}

